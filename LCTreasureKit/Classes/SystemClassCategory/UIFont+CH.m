#import "UIFont+CH.h"

@implementation UIFont (CH)

+ (UIFont *)fontOfPingFangSCMediumWithSize:(CGFloat)size
{
    return [UIFont fontWithName:@"PingFangSC-Medium" size:size];
}

+ (UIFont *)fontOfPingFangSCHeavyWithSize:(CGFloat)size
{
    return [UIFont fontWithName:@"PingFangSC-Heavy" size:size];
}


@end
